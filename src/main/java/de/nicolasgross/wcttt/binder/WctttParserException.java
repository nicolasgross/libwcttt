package de.nicolasgross.wcttt.binder;

public class WctttParserException extends Exception {

	public WctttParserException() {
		super();
	}

	public WctttParserException(String message) {
		super(message);
	}

	public WctttParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public WctttParserException(Throwable cause) {
		super(cause);
	}
}
