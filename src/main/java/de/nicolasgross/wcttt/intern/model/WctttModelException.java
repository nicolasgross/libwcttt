package de.nicolasgross.wcttt.intern.model;

public class WctttModelException extends Exception {

	public WctttModelException() {
		super();
	}

	public WctttModelException(String message) {
		super(message);
	}

	public WctttModelException(String message, Throwable cause) {
		super(message, cause);
	}

	public WctttModelException(Throwable cause) {
		super(cause);
	}
}
